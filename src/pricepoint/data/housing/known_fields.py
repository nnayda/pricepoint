"""Known property detail field names from Redfin listings.

Maps raw field names (as they appear in HTML) to normalized snake_case keys.
Built from a survey of 3,420 listing files producing 246 unique field names.
"""

from __future__ import annotations

import logging
import re

logger = logging.getLogger(__name__)


def _to_snake_case(name: str) -> str:
    """Convert a raw field name to snake_case.

    Handles ``#`` → ``num``, special characters → ``_``, trailing spaces, etc.
    """
    s = name.strip()
    # Replace '#' with 'num'
    s = s.replace("#", "num")
    # Replace special chars (except alphanumeric and spaces) with underscore
    s = re.sub(r"[^a-zA-Z0-9\s]", "_", s)
    # Replace whitespace with underscore
    s = re.sub(r"\s+", "_", s)
    # Collapse multiple underscores
    s = re.sub(r"_+", "_", s)
    # Lowercase and strip underscores
    s = s.lower().strip("_")
    return s


