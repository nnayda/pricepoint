# Pre-computed set of known snake_case values for fast reverse lookup
_KNOWN_SNAKE_VALUES: set[str] = set(KNOWN_FIELDS.values())


def normalize_field_name(raw_name: str) -> str:
    """Normalize a raw field name to its snake_case equivalent.

    Looks up in KNOWN_FIELDS first; falls back to ``_to_snake_case()``
    and logs a WARNING for unknown fields so we can track MLS schema drift.
    """
    if raw_name in KNOWN_FIELDS:
        return KNOWN_FIELDS[raw_name]

    snake = _to_snake_case(raw_name)
    if snake not in _KNOWN_SNAKE_VALUES:
        logger.warning("Unknown property detail field: %r (normalized to %r)", raw_name, snake)
    return snake
